// ===== HELPER FUNCTIONS =====

/**
 * Get emoji by category and key
 * @param {string} category - Category name (e.g., 'general', 'fish')
 * @param {string} key - Emoji key (e.g., 'success', 'shark')
 * @returns {string} Emoji or empty string if not found
 */
export function getEmoji(category, key) {
    return emojis[category]?.[key] || '';
}

/**
 * Get all emojis from a category
 * @param {string} category - Category name
 * @returns {object} Object containing all emojis in category
 */
export function getEmojiCategory(category) {
    return emojis[category] || {};
}

/**
 * Get random emoji from category
 * @param {string} category - Category name
 * @returns {string} Random emoji from category
 */
export function getRandomEmoji(category) {
    const categoryEmojis = emojis[category];
    if (!categoryEmojis) return '';
    
    const keys = Object.keys(categoryEmojis);
    const randomKey = keys[Math.floor(Math.random() * keys.length)];
    return categoryEmojis[randomKey];
}

/**
 * Check if emoji exists
 * @param {string} category - Category name
 * @param {string} key - Emoji key
 * @returns {boolean} True if emoji exists
 */
export function hasEmoji(category, key) {
    return !!(emojis[category]?.[key]);
}

