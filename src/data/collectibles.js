// Helper function to get all collectibles as flat array
export function getAllCollectibles() {
    const all = [];
    for (const category in collectiblesData) {
        all.push(...collectiblesData[category]);
    }
    return all;
}

// Helper function to get collectible by ID
export function getCollectibleById(id) {
    const all = getAllCollectibles();
    return all.find(c => c.id === id);
}

